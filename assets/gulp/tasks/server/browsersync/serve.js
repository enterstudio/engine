/**
 * Gulp tasks for serving site.
 */
const gulp = require('gulp');
const browserSync = require('browser-sync');

gulp.task('serve', (cb) => {
    const bs = browserSync.create();
    bs.init({
        open: true,
		online: true,
        port: 9000,
        https: false,
		online: true,
        server: {
            baseDir: ['.']
        }
    }, cb);
});

gulp.task('serve-secure', (cb) => {
    const bs = browserSync.create();
    bs.init({
        open: true,
		online: true,
        port: 9000,
        https: true,
		online: true,
        server: {
            baseDir: ['.']
        }
    }, cb);
});

