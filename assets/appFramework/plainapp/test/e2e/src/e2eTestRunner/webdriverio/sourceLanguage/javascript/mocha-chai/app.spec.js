import {expect} from "chai";

describe("App", () => {
    it("the title is set", () => {
        const uniteJson = require("../../../unite.json");
        return browser
            .url("/")
            .getTitle()
            .then((title) => {
                expect(title).to.equal(uniteJson.title);
            });
    });

    it("the body text is set", () => {
        return browser
            .url("/")
            .element("body")
            .getText()
            .then((bodyContent) => {
                expect(bodyContent).to.equal("Hello UniteJS World!");
            });
    });
});

