describe("App", () => {
    it("the title is set", () => {
        const uniteJson = require("../../../unite.json");
        return browser
            .url("/")
            .getTitle()
            .then((title) => {
                expect(title).toEqual(uniteJson.title);
            });
    });

    it("the body text is set", () => {
        return browser
            .url("/")
            .element("body")
            .getText()
            .then((bodyContent) => {
                expect(bodyContent).toEqual("Hello UniteJS World!");
            });
    });
});

