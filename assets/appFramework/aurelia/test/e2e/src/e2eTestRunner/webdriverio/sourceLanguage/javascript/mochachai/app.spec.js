/**
 * Tests for App.
 */
import {expect} from "chai";

describe("App", () => {
    it("the title is set", () => {
        const uniteJson = require("../../../../unite.json");
        return browser
            .url("/")
            .getTitle()
            .then((title) => {
                expect(title).to.equal(uniteJson.title);
            });
    });

    it("the router text is set", () => {
        return browser
            .loadAndWaitForAureliaPage("/")
            .element("router-view")
            .getText()
            .then((routerContent) => {
                expect(routerContent).to.equal("Hello UniteJS World!");
            });
    });
});

